function redirect()
{
  $redirect = BASE;
  if (isset($_GET["redirect"])) {
    $redirect = urldecode($_GET["redirect"]);
    if (!preg_match("#^" . BASE . ".*$#", $redirect)) {
      $redirect = BASE;
    }
  }
  header("Location: $redirect");
  die;
}

